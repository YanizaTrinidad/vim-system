public void loadData (){
        try{
            
            Class.forName("com.mysql.cj.jdbc.Driver");
            
            String url = "jdbc:MySQL://localhost:3306/dripcloud";
            
            String user = "root";
            String pass = "";
            
            Connection con = DriverManager.getConnection(url, user, pass);
            Statement st = con.createStatement();
            
            DefaultTableModel model = new DefaultTableModel(new String[]{"Product Name", "Product ID", "Product Type", "Quantity", "Price"}, 0);
            addTable.setModel(model);
            String sql = "SELECT * FROM stocks";
            ResultSet rs = st.executeQuery(sql);
            
            String pn, pid, d, q, p;
            while (rs.next()){
                
                
                pid = rs.getString("ID");
                d = rs.getString("product_type");
                pn = rs.getString("product_name");
                q = rs.getString("quantity");
                p = rs.getString("price");
                
                model.addRow(new Object[]{ pid, d, pn, q, p} );
                
            }
            
        }
        catch(Exception e){
            System.out.println("Error" + e.getMessage());
                    }
    }

