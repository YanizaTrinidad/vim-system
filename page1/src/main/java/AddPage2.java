    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        String pn, d, q, p, query;
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            String url = "jdbc:MySQL://localhost:3306/dripcloud";
            String user = "root";
            String pass = "";
            Connection con = DriverManager.getConnection(url, user, pass);
            Statement st = con.createStatement();
            
            if("".equals(prdtype.getText())){
                
                JOptionPane.showMessageDialog(new JFrame(), "Product type is required", "Dialog", 
                        JOptionPane.ERROR_MESSAGE);
            }else if("".equals(prdname.getText())){
                
                JOptionPane.showMessageDialog(new JFrame(), "Product name is required", "Dialog", 
                        JOptionPane.ERROR_MESSAGE);
            }else if("".equals(quantity.getText())){
                
                JOptionPane.showMessageDialog(new JFrame(), "Quantity is required", "Dialog", 
                        JOptionPane.ERROR_MESSAGE);
            }else if("".equals(price.getText())){
                
                JOptionPane.showMessageDialog(new JFrame(), "Price is required", "Dialog", 
                        JOptionPane.ERROR_MESSAGE);
            }else{
                
                d = prdtype.getText();
                pn = prdname.getText();
                q = quantity.getText();
                p = price.getText();
                
                query = "INSERT INTO stocks (product_type, product_name, qty, price) " 
                        + "VALUES ('"+d+"','"+pn+"','"+q+"','"+p+"')";
                        
                st.executeUpdate(query);
                prdtype.setText("");
                prdname.setText("");
                quantity.setText("");
                price.setText("");
                
            
                showMessageDialog(null, "Successfully registered.");
                
                loadData();
                
                con.close();
                    
            }
            
        }catch(Exception e){
            System.out.println("Error" + e.getMessage());
        }

    }//GEN-LAST:event_addButtonActionPerformed

